"""
определить тип результата перечисленных выражений

"""


print("Выражение: 15 * 3; результат: ", 15 * 3, "; тип: ", type(15 * 3))
print("Выражение: 15 / 3; результат: ", 15 / 3, "; тип: ", type(15 / 3))
print("Выражение: 15 // 2; результат: ", 15 // 2, "; тип: ", type(15 // 2))
print("Выражение: 15 ** 2; результат: ", 15 ** 2, "; тип: ", type(15 ** 2))